<h2>CLC di Indonesia</h2>

<p>
Christian Life Community (CLC) berakar pada kelompok-kelompok orang awam (pria dan wanita, tua dan muda, dari segala kelompok
 sosial) yang saling mendukung dalam menanggapi panggilan untuk mencintai Allah dengan menegaskan cara yang paling tepat dan
 khas sesuai dengan situasi yang ada. 
</p>
<p>
Pada mulanya komunitas ini bernama Kongregasi Maria, didirikan oleh Pater Jean Leunis, Sj dan diakui oleh gereja dengan Bulla
 “Omnipotentis Dei” pada tahun 1565. Kelompok pertama yang secara resmi diakui Gereja adalah kelompok mahasiswa di Collegium
 Romanum. Pada tahun 1967 seiring dengan semangat pembaruan pada Konsili Vatican II, nama tersebut diubah menjadi CLC yang
 dirasa lebih tepat mengungkapkan identitasnya. 
</p>
<p>
Di Indonesia, CLC juga pernah dikenal dengan nama Komunitas Hidup Kristiani (KHK) sejak tahun 1980 hingga  2007. Pada SUN ke XI
 tahun 2007, komunitas memilih dan memutuskan untuk menggunakan nama internasional CLC, untuk menunjukkan semangat persatuan
 dengan komunitas dunia. Saat ini terdapat lebih dari 20.000 pria dan wanita, tua dan muda, yang menjadi anggota CLC di seluruh
 penjuru dunia. 
</p>
<p>
CLC khususnya cocok bagi mereka yang ingin mengungkapkan kasih kepada Allah ditengah kesibukan dunia sehari-hari, sehingga
 semakin kokoh pengabdiannya kepada sesama dalam lingkungan keluarga, masyarakat, profesi maupun dalam lingkungan Gereja. 
</p>
<p>
Siapa saja yang ingin bergabung dengan CLC diterima dengan senang hati dan tangan terbuka.
</p>

<blockquote>
Our first mission is the ordinary life, finding and serving God in all our daily activities as work, family, studies, etc .
<br/>
Our apostolic life, through institutions, can be extended and complements that first mission.
</blockquote>
